    <script>
        let currentStep = 1;
        const totalSteps = 4;
        
        function updateProgress() {
            const progress = (currentStep / totalSteps) * 100;
            document.getElementById('progressFill').style.width = progress + '%';
        }
        
        function showStep(step) {
            document.querySelectorAll('.step').forEach(s => s.classList.remove('active'));
            document.querySelector(`[data-step="${step}"]`).classList.add('active');
            
            document.getElementById('prevBtn').style.display = step > 1 ? 'block' : 'none';
            document.getElementById('nextBtn').textContent = step === 3 ? 'Complete Setup' : 'Next ‚Üí';
            
            updateProgress();
        }
        
        function validateStep(step) {
            const inputs = document.querySelectorAll(`[data-step="${step}"] input[required]`);
            let valid = true;
            
            inputs.forEach(input => {
                if (!input.value.trim()) {
                    valid = false;
                    input.style.borderColor = '#ef4444';
                } else {
                    input.style.borderColor = '#e0e0e0';
                }
            });
            
            // Special validation for step 2 (password match)
            if (step === 2) {
                const pass = document.getElementById('admin_password').value;
                const confirm = document.getElementById('admin_password_confirm').value;
                
                if (pass !== confirm) {
                    alert('Passwords do not match!');
                    return false;
                }
                
                if (pass.length < 8) {
                    alert('Password must be at least 8 characters!');
                    return false;
                }
            }
            
            if (!valid) {
                alert('Please fill in all required fields');
            }
            
            return valid;
        }
        
        async function saveStep(step) {
            const formData = new FormData();
            formData.append('step', step);
            
            const inputs = document.querySelectorAll(`[data-step="${step}"] input`);
            inputs.forEach(input => {
                formData.append(input.name, input.value);
            });
            
            try {
                const response = await fetch('setup.php', {
                    method: 'POST',
                    body: formData,
                    headers: {
                        'X-Requested-With': 'XMLHttpRequest'
                    }
                });
                
                return response.ok;
            } catch (error) {
                console.error('Save failed:', error);
                return false;
            }
        }
        
        document.getElementById('nextBtn').addEventListener('click', async () => {
            if (!validateStep(currentStep)) {
                return;
            }
            
            // Save current step
            const saved = await saveStep(currentStep);
            
            if (!saved && currentStep < 4) {
                alert('Failed to save. Please try again.');
                return;
            }
            
            if (currentStep < totalSteps) {
                currentStep++;
                showStep(currentStep);
                
                // Trigger installation on step 4
                if (currentStep === 4) {
                    setTimeout(() => {
                        window.location.reload();
                    }, 2000);
                }
            }
        });
        
        document.getElementById('prevBtn').addEventListener('click', () => {
            if (currentStep > 1) {
                currentStep--;
                showStep(currentStep);
            }
        });
        
        // Initial setup
        updateProgress();
